public class Concatenacao {
	
	public static void main (String[] args) {
		
		System.out.println( "Java"+ 1 + 2 + 3 );
		System.out.println("Java" + (1 + 2 + 3)  );
		System.out.println(  "123" + "Java" );
		System.out.println( ( 1 + 2 + 3 ) +  "Java" );
		
	}
}

